package smallStudyGroep_SSG;

public class For_Loop02 {
    public static void main(String[] args) {

        for (int i=0; i<101; i+=2) {
            System.out.print(i+",");
        }
        System.out.println();
        for (int k=1; k<101; k+=2) {
            System.out.print(k+",");
        }
    }
}
